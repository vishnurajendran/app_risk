package game.States;

/**
 * This enum provides the states of the game
 * at a given point
 *
 * @author Soham
 */
public enum GameStates {

    /**
     * Starting state of game.
     */
    GameStart,

    /**
     * Deploying state of the game.
     */
    IssueOrder,

    /**
     * Executing state of the game.
     */
    ExecuteOrder,

    /**
     * GameOver state
     */
    GameOver,

}
